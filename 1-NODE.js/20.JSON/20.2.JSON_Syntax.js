/* 
# JSON Syntax is a subset of the JavaScript syntax -->
JSON syntax is derived from JavaScript object notation syntax:
+ Data is in name/value pairs.
+ Data is separated by commas.
+ Curly braces hold objects.
+ Square brackets hold arrays.

-- JSON data is written as name/value pairs.
   "name":"John"
   note: JSON names require double quotes. JavaScript names don't.

-- JSON - Evaluates to JavaScript Objects
   + In JSON, keys must be strings, written with double quotes:
   { "name":"John" }

   + In JavaScript, keys can be strings, numbers, or identifier names:
   { name:"John" }

-- JSON Values- In JSON, values must be one of the following data types:
   + a string
   + a number
   + an object (JSON object)
   + an array
   + a boolean
   + null
   in JSON the value cannot be of the following type: 
   + a function
   + a date
   + undefined
   in JS the value can of any above listed type ..

*/
